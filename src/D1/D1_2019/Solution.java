package D1.D1_2019;/////////////////////////////////////////////////////////////////////////////////////////////


import java.util.Scanner;

class Solution
{
    public static void main(String args[]) throws Exception
    {

        Scanner sc = new Scanner(System.in);
        int T = sc.nextInt();
        int sum=1;
        for(int i=0;i<=T;i++){
            System.out.print(sum+" ");
            sum *=2;

        }




    }
}