package D1.D1_2025;/////////////////////////////////////////////////////////////////////////////////////////////


import java.util.Scanner;

class Solution
{
    public static void main(String args[]) throws Exception
    {

        Scanner sc = new Scanner(System.in);
        int T = sc.nextInt();

        int result=0;

        for(int i=1;i<=T;i++){
            result +=i;
        }
        System.out.println(result);




    }
}